package post;

public class Post {
	private int pNum;
	private String pTitle;
	private String wNum;
	private String pContext;
	private String pDate;
	private int pAvaliable;
	public String getwNum() {
		return wNum;
	}
	public void setwNum(String wNum) {
		this.wNum = wNum;
	}
	public String getpTitle() {
		return pTitle;
	}
	public void setpTitle(String pTitle) {
		this.pTitle = pTitle;
	}
	public String getpContext() {
		return pContext;
	}
	public void setpContext(String pContext) {
		this.pContext = pContext;
	}
	public String getpDate() {
		return pDate;
	}
	public void setpDate(String pDate) {
		this.pDate = pDate;
	}
	public int getpAvaliable() {
		return pAvaliable;
	}
	public void setpAvaliable(int pAvaliable) {
		this.pAvaliable = pAvaliable;
	}
	public int getpNum() {
		return pNum;
	}
	public void setpNum(int pNum) {
		this.pNum = pNum;
	}
	
}
